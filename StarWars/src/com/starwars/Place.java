package com.starwars;

public class Place {
	//class variables
	public String name;
	public boolean isEmpire;
	
	//getters + setters
	public String getName() {
		return this.name;
	}
	public void setName(String n) {
		this.name = n;
	}
	public boolean getEmpire() {
		return this.isEmpire;
	}
	public void setEmpire(boolean e) {
		this.isEmpire = e;
	}
}
